package de.janrieke.contractmanager.gui.action;

import de.willuhn.jameica.gui.Action;
import de.willuhn.jameica.gui.GUI;
import de.willuhn.jameica.gui.internal.views.Settings;
import de.willuhn.util.ApplicationException;

public class ShowJameicaSettings implements Action {

	private String tab;

	public ShowJameicaSettings(String tab) {
		this.tab = tab;
	}

	/**
	 * @see de.willuhn.jameica.gui.Action#handleAction(java.lang.Object)
	 */
	public void handleAction(Object context) throws ApplicationException {
		GUI.startView(Settings.class, tab);
	}
}